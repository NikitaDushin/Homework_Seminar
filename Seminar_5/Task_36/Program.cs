// Найдите сумму элементов, стоящих на нечётных позициях.
// [3, 7, 23, 12] -> 19
// [-4, -6, 89, 6] -> 0


int[] creatRandomArray(int N, int start, int end)
{
    int[] RandomArray = new int[N];
    for (int i = 0; i < N; i++)
    {
        RandomArray[i] = new Random().Next(start, end + 1);
    }
    return RandomArray;
}

void ShowArray(int[] array)
{
    Console.Write("[ ");
    for (int i = 0; i < array.Length; i++)
    {
        Console.Write(array[i] + " ");
    }
    Console.WriteLine("]");
}


int[] NewArray = creatRandomArray(11, 0, 10);
ShowArray(NewArray);
int Sum = 0;
for(int i = 1; i<NewArray.Length; i+= 2)
{
    Sum = Sum + NewArray[i];
}
Console.Write(Sum);

